radio = int(input("Ingrese la longitud del radio: "))
pi = 3.14
superficie = pi * radio**2
perimetro = pi * (radio *2)
superficieEsfera = 4* pi * (radio**2)
volumen = 4/3 * pi * (radio **3)
print("La superficie del circulo es ", superficie)
print("El permietro de la circunsferencia es ", perimetro)
print("La superficie de la esfera es ", superficieEsfera)
print("El volumen de la esfera es ", volumen)
